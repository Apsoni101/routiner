extension EmojiExtension on Emoji {
  /// Returns the emoji symbol as a String
  String get emoji => symbol;

  /// Returns the emoji symbol as a Text widget
  Text toText({final double? fontSize, final TextStyle? style}) {
    return Text(
      symbol,
      style:
          style?.copyWith(fontSize: fontSize) ?? TextStyle(fontSize: fontSize),
    );
  }

  /// Returns the name of the emoji in a readable format
  String get displayName {
    return name
        .replaceAllMapped(RegExp('([A-Z])'), (final Match match) => ' ${match.group(0)}')
        .trim()
        .replaceAll(RegExp(r'\d+'), '')
        .toLowerCase()
        .split(' ')
        .map(
          (final String word) =>
              word.isEmpty ? '' : word[0].toUpperCase() + word.substring(1),
        )
        .join(' ');
  }
}
